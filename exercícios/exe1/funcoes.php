<?php

function somar(float $primeiroValor, float $segundoValor): float
{
    return $primeiroValor + $segundoValor;
}
function subtrair(float $primeiroValor, float $segundoValor): float
{
    return $primeiroValor - $segundoValor;
}
function multiplicar(float $primeiroValor, float $segundoValor): float
{
    return $primeiroValor * $segundoValor;
}
function dividir(float $primeiroValor, float $segundoValor): float
{
    return $primeiroValor / $segundoValor;
}